    private void guardar() {
        if (cjNombre.getText().isEmpty() || cjApellido.getText().isEmpty() || cjTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Alg√∫n dato esta vacio. !");
            return ;
        }
        OperCliente oper = new OperCliente();
        Cliente cli = new Cliente();
        cli.modNombre(cjNombre.getText());
        cli.modApellido(cjApellido.getText());
        Long telf = 0L;
        try{
            telf = Long.parseLong(cjTelefono.getText().trim());
            cli.modTelefono(telf);
        }catch(NumberFormatException nu){
            
        }
        if (oper.registrar(cli)){
            JOptionPane.showMessageDialog(this, "Se registro el cliente");
        }else{
            JOptionPane.showMessageDialog(this, "No se registro el cliente");

        }
    }
